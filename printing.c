#include <stdio.h>
int main()
{
    char chr = 'a';    
    int number1= 5;
    float number2 = 13.5;
    double number3 = 12.4;

    printf("character = %c\n", chr); 
    printf("Number = %d\n", number1);
    printf("number1 = %f\n", number2);
    printf("number2 = %lf\n", number3);

    return 0;
}
